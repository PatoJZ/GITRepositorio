#include <stdio.h>

int main() {
 
  int entero = 10;
  float flotante = 2.12;
  double doble = 3.14159265359;
  char caracter = 'A';

  printf("Dato int: %d\n", entero);
  printf("Dato float: %f\n", flotante);
  printf("Dato double: %lf\n", doble);
  printf("Dato char: %c\n", caracter);

  return 0;
}